// package Day 9;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 2; i <= 10; i+=2) {
            System.out.println("Count is: " + i);
        }
    }
}

// i+=2 -> i = i + 2
// For Loop -> Jab hume pata hota hai ki loop kitni baar chalana hai, tab hum for loop ka use karte hain. For loop me initialization, condition check, aur increment/decrement ek hi line me likhe jaate hain, jo code ko concise aur readable banata hai.